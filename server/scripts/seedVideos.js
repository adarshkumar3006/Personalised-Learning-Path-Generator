const seedVideos = async () => {
  try {
    await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/learning-path-generator');
    console.log('Connected to MongoDB');

    // Clear existing videos (optional - remove if you want to keep existing)
    // await Video.deleteMany({});

    // Insert videos
    for (const videoData of sampleVideos) {
      await Video.findOneAndUpdate(
        { videoId: videoData.videoId },
        videoData,
        { upsert: true, new: true, setDefaultsOnInsert: true }
      );
    }

    console.log(`âœ… Seeded ${sampleVideos.length} videos`);
    process.exit(0);
  } catch (error) {
    console.error('Error seeding videos:', error);
    process.exit(1);
  }
};

